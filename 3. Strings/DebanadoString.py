cadena = 'Este es un ejemplo de Substring (Debanado de Cadenas)'

print(len(cadena))
print(cadena[50])

print(cadena[0 : 12]) #Debanado o Substring muestra hasta 1 antes, en este caso de 0 a 11
print(cadena[6 : ]) #Muestra desde 6 hasta el final
print(cadena[-8 : ]) #muestra desde el final -8 hasta 0